// A função de multiplicação 
function multiplicacao(num1: number, num2: number): number {
    return num1 * num2;
}

// A função de saudação 
function saudacao(nome: string): string {
    return 'Olá, ' + nome;
}

// Retorno
console.log(multiplicacao(10, 3)); // Saída: 30
console.log(saudacao('Isabela'));    // Saída: Olá Maria